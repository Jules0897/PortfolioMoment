//zorgt dat de navigatie balk geopent kan worden door op het open icoontje te drukken
function openNav() 
{
  document.getElementById("mySidenav").style.width = "250px";
}
//zorgt ervoor dat het kruisje op de navigatiebalk werkt
function closeNav() 
{
  document.getElementById("mySidenav").style.width = "0";
}